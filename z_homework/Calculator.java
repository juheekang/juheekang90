package z_homework;

public class Calculator {
//	다음을 한줄식 계산해서 최종 결과값을 출력해주세요.
//	1. 123456 + 654321
//	2. 1번의 결과값 * 123456
//	3. 2번의 결과값 / 123456
//	4. 3번의 결과값 - 654321
//	5. 4번의 결과값 % 123456
//	* 반드시 Calculator Class를 생성하고, 각 계산에 필요한 메서드를 생성/호출 하세요.
//
//
//	1. 777777.0
//	2. 9.6021237312E10
//	3. 777777.0
//	4. 123456.0
//	5. 0.0

	double add(double a, double b) {
		return a + b;
	}
	double multiply(double a, double b) {
		return a * b;
	}
	double divide(double a, double b) {
		return a / b;
	}
	double subtract(double a, double b) {
		return a - b;
	}
	double remainder(double a, double b) {
		return a % b;
	}
}
